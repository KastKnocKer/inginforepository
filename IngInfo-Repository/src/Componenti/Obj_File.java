package Componenti;

public class Obj_File {
	
	private String Nome;
	private String Path;
	private String Url;
	
	
	public Obj_File(){
		
	}


	public void setNome(String nome) {
		Nome = nome;
	}


	public String getNome() {
		return Nome;
	}


	public void setPath(String path) {
		Path = path;
	}


	public String getPath() {
		return Path;
	}


	public void setUrl(String url) {
		Url = url;
	}


	public String getUrl() {
		return Url;
	}

}
